#include "libft.h"

size_t		ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	if (!(dst) || !(src))
		return (0);
	if (!size)
		return (ft_strlen(src));
	i = 0;
	while (src[i] && size > i + 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	while (src[i])
		i++;
	return (i);
}
