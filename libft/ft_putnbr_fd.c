#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			tmp;
	unsigned int	nb;

	if (n < 0)
	{
		nb = -n;
		ft_putchar_fd('-', fd);
	}
	else
		nb = n;
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		tmp = nb + '0';
		ft_putchar_fd(tmp, fd);
	}
}
