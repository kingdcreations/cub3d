#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*res;

	i = 0;
	while (s1[i])
		i++;
	if (!(res = malloc(sizeof(char) * (i + 1))))
		return (0);
	i = 0;
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
